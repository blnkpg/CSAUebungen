package uebung3;

public class Futter {

	int fID = 0;
	double nutrition = 0;
	String color = "";
	String name = "";
	
	/**
	 * 
	 * @param nutrition
	 * @param color
	 * @param name
	 */
	public Futter(int fID, double nutrition, String color, String name) {
		this.fID = fID; 
		this.nutrition = nutrition;
		this.color = color;
		this.name = name;
	}
	
	
}
